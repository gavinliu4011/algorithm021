// @lc code=start
package main

func isValid(s string) bool {
	if len(s)%2 == 1 || len(s) == 0 {
		return false
	}
	bracketMap := map[byte]byte{
		')': '(',
		']': '[',
		'}': '{',
	}

	stack := make([]byte, 0)
	for i := 0; i < len(s); i++ {
		if bracketMap[s[i]] > 0 {
			if len(stack) == 0 || stack[len(stack)-1] != bracketMap[s[i]] {
				return false
			}
			stack = stack[:len(stack)-1]
		} else {
			stack = append(stack, s[i])
		}
	}

	return len(stack) == 0
}

